    public void validaFormulario(){
        /* 
        * Nesta estrutura de seleÃ§Ã£o aninhada verificaremos se existe algum campo que nÃ£o foi preenchido.
        * Obs.: Campos de texto - obtemos o valor digitado no campo pelo comando getText();
        * Obs.: O equals("") verifica se o valor obtido do formulÃ¡rio Ã© igual a "" (vazio ou em branco). 
        */
        if (cpf.getText().equals("   .   .   -  ")){
            JOptionPane.showMessageDialog(null, "Favor informar o CPF!", "Mensagem de Erro", 0);
            cpf.requestFocus();
        } else if (nome.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Favor informar seu Nome!", "Mensagem de Erro", 0);
            nome.requestFocus();
        } else if (email.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Favor informar o E-mail!", "Mensagem de Erro", 0);
            email.requestFocus();
        } else if (telefone.getText().equals("(  )      -    ")){
            JOptionPane.showMessageDialog(null, "Favor informar o telefone!", "Mensagem de Erro", 0);
            telefone.requestFocus();
        } else if (logradouro.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Favor informar o logradouro!", "Mensagem de Erro", 0);
            logradouro.requestFocus();
        } else if (email.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Favor informar o nÃºmero!", "Mensagem de Erro", 0);
            numero.requestFocus();
        } else if (complemento.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Favor informar o complemento!", "Mensagem de Erro", 0);
            complemento.requestFocus();
        } else if (bairro.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Favor informar o bairro!", "Mensagem de Erro", 0);
            bairro.requestFocus();
        } else if (cidade.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Favor informar a cidade!", "Mensagem de Erro", 0);
            cidade.requestFocus();
        } else if (uf.getSelectedItem().equals("Selecione")){
            JOptionPane.showMessageDialog(null, "Favor informar a UF!", "Mensagem de Erro", 0);
            uf.requestFocus();
        } else if (cep.getText().equals("     -   ")){
            JOptionPane.showMessageDialog(null, "Favor informar o CEP!", "Mensagem de Erro", 0);
            cep.requestFocus();
        } else {
            cadastraCliente(); // Chama o mÃ©todo para cadastrar o Cliente no banco de dados;
        }
    }
    
    // O mÃ©todo criaObjetoCliente vai criar um objeto de Cliente que posteriormente serÃ¡ cadastrado no banco de dados;
    public void criaObjetoCliente(Cliente cliente){
        // Armazenar os dados obtidos do formulÃ¡rio num objeto Cliente
        cliente.setCpf(cpf.getText()); // Escreve na variÃ¡vel cpf do objeto Cliente o dado que foi obtido do campo cpf no formulÃ¡rio;
        cliente.setNome(nome.getText()); // Repete para as demais variÃ¡veis
        cliente.setEmail(email.getText());
        cliente.setTelefone(telefone.getText());
        
        // Armazena os dados de endereÃ§o obtidos do formulÃ¡rio num objeto endereÃ§o
        endereco.setLogradouro(logradouro.getText());
        endereco.setNumero(Integer.parseInt(numero.getText()));
        endereco.setComplemento(complemento.getText());
        endereco.setBairro(bairro.getText());
        endereco.setCidade(cidade.getText());
        int indice = uf.getSelectedIndex(); // Armazena o Ã­ndice do da UF selecionada
        // O getItemAt pega o valor literal na posiÃ§Ã£o do Ã­ndice 
        endereco.setUf((String)uf.getItemAt(indice)); // Por ser um objeto o valor deve ser transformado numa String pra ser armazenado no banco de dados. Por isso o cast de String. (String)
        endereco.setCep(cep.getText());
        endereco.setClienteCpf(cpf.getText());
        
        cliente.setEndereco(endereco); // Passa todos os dados do objeto endereco para o objeto cliente. Porque cliente tem uma variÃ¡vel do tipo Endereco
    }
    
    public void cadastraCliente(){
        criaObjetoCliente(cliente);
        
        ClienteDao.cadastrar(cliente);
        ClienteDao.cadastrarEnderecoCliente(cliente);
        limparCamposDoFormulario();
        }
    
